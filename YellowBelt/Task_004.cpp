auto MakeRegionTuple(const Region& r) {
	return std::tie(r.std_name, r.parent_std_name, r.names, r.population);
}

bool operator<(const Region& lhs, const Region& rhs) {
	return MakeRegionTuple(lhs) < MakeRegionTuple(rhs);
}

int FindMaxRepetitionCount(const std::vector<Region>& regions) {
	std::map<Region, int> matchСounter {};

	int maxValue = 0;
	for (const auto& region : regions) {
		maxValue = std::max(maxValue, ++matchСounter[region]);
	}//rof

	return maxValue;
}

